            $act = ''; 
        }

        if ($act=='add') {

            if($this->core->loadDisabledMenu('berkas_digital_perawatan')['create'] == 'true') {
              http_response_code(403);
              $data = array(
                'code' => '403', 
                'status' => 'error', 
                'msg' => 'Maaf, akses dibatasi!'
              );
              echo json_encode($data);    
              exit();
            }

        $no_rawat = $_POST['no_rawat'];
$kode = $_POST['kode'];
$lokasi_file = $_POST['lokasi_file'];

            
