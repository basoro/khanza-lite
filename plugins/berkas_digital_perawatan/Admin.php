    public function getChart($type = '', $column = '')
    {
      if($type == ''){
        $type = 'pie';
      }

      $labels = $this->core->db->select('berkas_digital_perawatan', 'kode', ['GROUP' => 'kode']);
      $datasets = $this->core->db->select('berkas_digital_perawatan', ['count' => \Medoo\Medoo::raw('COUNT(<kode>)')], ['GROUP' => 'kode']);

      if(isset_or($column)) {
        $labels = $this->core->db->select('berkas_digital_perawatan', ''.$column.'', ['GROUP' => ''.$column.'']);
        $datasets = $this->core->db->select('berkas_digital_perawatan', ['count' => \Medoo\Medoo::raw('COUNT(<'.$column.'>)')], ['GROUP' => ''.$column.'']);          
      }

      $database = DBNAME;
      $nama_table = 'berkas_digital_perawatan';

      $get_table = $this->core->db->pdo->prepare("SELECT COLUMN_NAME FROM information_schema.COLUMNS WHERE TABLE_SCHEMA='$database' AND TABLE_NAME='$nama_table'");
	    $get_table->execute();
	    $result = $get_table->fetchAll();

      if($this->settings('settings', 'logquery') == true) {
        $this->core->LogQuery('berkas_digital_perawatan => getChart');
      }

      echo $this->draw('chart.html', ['type' => $type, 'column' => $result, 'labels' => json_encode($labels), 'datasets' => json_encode(array_column($datasets, 'count'))]);
      exit();
    }

