    public function postData()
    {
        $column_name = isset_or($_POST['column_name'], 'no_rawat');
        $column_order = isset_or($_POST['column_order'], 'asc');
        $draw = isset_or($_POST['draw'], '0');
        $row1 = isset_or($_POST['start'], '0');
        $rowperpage = isset_or($_POST['length'], '10'); // Rows display per page
        $columnIndex = isset_or($_POST['order'][0]['column']); // Column index
        $columnName = isset_or($_POST['columns'][$columnIndex]['data'], $column_name); // Column name
        $columnSortOrder = isset_or($_POST['order'][0]['dir'], $column_order); // asc or desc
        $searchValue = isset_or($_POST['search']['value']); // Search value

        ## Custom Field value
        $search_field_berkas_digital_perawatan= isset_or($_POST['search_field_berkas_digital_perawatan']);
        $search_text_berkas_digital_perawatan = isset_or($_POST['search_text_berkas_digital_perawatan']);

        if ($search_text_berkas_digital_perawatan != '') {
          $where[$search_field_berkas_digital_perawatan.'[~]'] = $search_text_berkas_digital_perawatan;
          $where = ["AND" => $where];
        } else {
          $where = [];
        }

        ## Total number of records without filtering
        $totalRecords = $this->core->db->count('berkas_digital_perawatan', '*');

        ## Total number of records with filtering
        $totalRecordwithFilter = $this->core->db->count('berkas_digital_perawatan', '*', $where);

        ## Fetch records
        $where['ORDER'] = [$columnName => strtoupper($columnSortOrder)];
        $where['LIMIT'] = [$row1, $rowperpage];
        $result = $this->core->db->select('berkas_digital_perawatan', '*', $where);

        $data = array();
        foreach($result as $row) {
            $data[] = array(
                'no_rawat'=>$row['no_rawat'],
'kode'=>$row['kode'],
'lokasi_file'=>$row['lokasi_file']

            );
        }

        ## Response
        http_response_code(200);
        $response = array(
            "draw" => intval($draw), 
            "iTotalRecords" => $totalRecords,
            "iTotalDisplayRecords" => $totalRecordwithFilter,
            "aaData" => $data
        );

        if($this->settings('settings', 'logquery') == true) {
          $this->core->LogQuery('berkas_digital_perawatan => postData');
        }

        echo json_encode($response);
        exit();
    }

    public function postAksi()
